class Person:
    def __init__(self, ID, NAME, EMAIL, PHONE):
        self.PHONE = PHONE
        self.EMAIL = EMAIL
        self.NAME = NAME
        self.ID = ID


class Gadget:
    def __init__(self, ID, BRAND, TYPE, MODEL):
        self.MODEL = MODEL
        self.TYPE = TYPE
        self.BRAND = BRAND
        self.ID = ID

